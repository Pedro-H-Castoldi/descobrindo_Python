"""
Programação Orientada a Objeto - POO

Poo é um paradigma de programação que utiliza mapeamentos de objetos do mundo real para modelos computacionais.

Paradigma de programação é a forma/metodologia utilizada para pensar/desenvolver sistemas.

Principais elementos da  POO:
    - Classes -> Modelo do objeto do mundo real sendo representado computacionalmente;
    - Atributo -> Características do objeto;
    - Método -> Comportamento do objeto (funções);
    - Construtor -> Método especial utilizado para criar os objetos;
    - Objeto -> Instância da classe.
"""

# Definindo variáveis
numero = 10
print(10)
print(type(numero))

# Definindo uma classe
class Produto:
    pass

# Definindo objeto da classe Produto
ps5 = Produto() # Produto() é um contrutor, do qual exerce sua função de criar um objeto.
print(ps5)
print(type(ps5))